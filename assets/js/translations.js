// Current language state
let currentLang = "en";

/**
 * Initialize language system
 */
function initLanguage() {
  const savedLang = localStorage.getItem("fwa_language");

  if (savedLang) {
    currentLang = savedLang;
    applyLanguage(currentLang);
  } else {
    // English is the default - open in English without asking visitor to choose
    currentLang = "en";
    localStorage.setItem("fwa_language", "en");
    applyLanguage("en");
  }
}

/**
 * Show language selection modal
 */
function showLanguageModal() {
  const modal = document.getElementById("lang-modal");
  if (modal) {
    modal.classList.add("active");
    document.body.style.overflow = "hidden";
  }
}

/**
 * Hide language selection modal
 */
function hideLanguageModal() {
  const modal = document.getElementById("lang-modal");
  if (modal) {
    modal.classList.remove("active");
    document.body.style.overflow = "";
  }
}

/**
 * Set language and apply changes
 */
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("fwa_language", lang);
  localStorage.setItem("fwa_language_selected", "true");
  applyLanguage(lang);
  hideLanguageModal();
}

/**
 * Apply language to page
 */
function applyLanguage(lang) {
  const html = document.documentElement;

  html.setAttribute("dir", lang === "he" ? "rtl" : "ltr");
  html.setAttribute("lang", lang);

  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const translation = getTranslation(key, lang);
    if (translation) {
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = translation;
      } else {
        // Use innerHTML to support HTML tags like <strong>, <br>, etc.
        el.innerHTML = translation;
      }
    }
  });

  updateToggleButtons(lang);
}

/**
 * Get translation by key path
 */
function getTranslation(keyPath, lang) {
  const keys = keyPath.split(".");
  let value = translations[lang];
  if (!value || typeof value !== "object") return "";
  for (const key of keys) {
    if (
      value !== null &&
      typeof value === "object" &&
      Object.prototype.hasOwnProperty.call(value, key)
    ) {
      value = value[key];
    } else {
      return "";
    }
  }
  return typeof value === "string" ? value : "";
}

/**
 * Update toggle button states
 */
function updateToggleButtons(lang) {
  const buttons = document.querySelectorAll(".lang-toggle__btn");
  buttons.forEach((btn) => {
    if (btn.getAttribute("data-lang") === lang) {
      btn.classList.add("active");
    } else {
      btn.classList.remove("active");
    }
  });
}

/**
 * Get current language
 */
function getCurrentLang() {
  return currentLang;
}

/**
 * Toggle between languages
 */
function toggleLanguage() {
  const newLang = currentLang === "en" ? "he" : "en";
  setLanguage(newLang);
}

// Initialize on DOM ready
document.addEventListener("DOMContentLoaded", initLanguage);

// Export for use in other scripts
window.FWALang = {
  setLanguage,
  getCurrentLang,
  getTranslation,
  toggleLanguage,
};
